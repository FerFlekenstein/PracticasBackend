import passport from "passport";
import { Router } from "express";
import uploader from "../services/upload.js";
import sessionController from "../controllers/session.controller.js";
const router = Router();
router.post('/register', uploader.single('foto'), sessionController.registro);
router.post('/', passport.authenticate("login", {failureRedirect: "/fail", session: false}), sessionController.createToken);
router.get("/fail", sessionController.fail);
router.get('/logout', sessionController.clearAndRedirect);
export default router;